// ── CONTACT ────────────────────────────────────────────────
function buildContact() {
  const rows = [
    { icon: 'fa-phone',        label: 'Phone',    value: CONFIG.phone,    href: `tel:${CONFIG.phone}` },
    { icon: 'fa-location-dot', label: 'Based In', value: `${CONFIG.location}, ${CONFIG.county}` },
    { icon: 'fa-map',          label: 'Coverage', value: CONFIG.coverage },
    { icon: 'fa-clock',        label: 'Hours',    value: CONFIG.hours },
  ];

  if (CONFIG.email) {
    rows.splice(1, 0, { icon: 'fa-envelope', label: 'Email', value: CONFIG.email, href: `mailto:${CONFIG.email}` });
  }

  document.getElementById('contact-details').innerHTML = rows.map(row => {
    const inner = `
      <div class="contact-icon"><i class="fa-solid ${row.icon}"></i></div>
      <div>
        <span class="contact-label">${row.label}</span>
        <span class="contact-value">${row.value}</span>
      </div>
    `;
    return row.href
      ? `<a href="${row.href}" class="contact-item">${inner}</a>`
      : `<div class="contact-item">${inner}</div>`;
  }).join('');
}

