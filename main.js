// ── SHARED LOGO HTML ───────────────────────────────────────
function logoHTML() {
  const [first, ...rest] = CONFIG.businessName.split('&');
  if (rest.length) {
    return `
      <span class="logo-sb">${first.trim()}<span class="amp">&amp;</span>${rest.join('&').trim()}</span>
      <span class="logo-text">${CONFIG.tagline}</span>
    `;
  }
  return `
    <span class="logo-sb">${CONFIG.businessName}</span>
    <span class="logo-text">${CONFIG.tagline}</span>
  `;
}

// ── INTERACTIONS ───────────────────────────────────────────
function initInteractions() {
  // Navbar scroll effect
  const navbar = document.getElementById('navbar');
  window.addEventListener('scroll', () => {
    navbar.classList.toggle('scrolled', window.scrollY > 60);
  });

  // Mobile burger menu
  const burger = document.getElementById('burger');
  const navLinks = document.getElementById('nav-links');
  burger.addEventListener('click', () => navLinks.classList.toggle('open'));
  navLinks.querySelectorAll('a').forEach(link => {
    link.addEventListener('click', () => navLinks.classList.remove('open'));
  });

  // Scroll-reveal for service cards
  const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (!entry.isIntersecting) return;
      const el = entry.target;
      setTimeout(() => el.classList.add('visible'), parseInt(el.dataset.delay || '0', 10));
      revealObserver.unobserve(el);
    });
  }, { threshold: 0.15 });

  document.querySelectorAll('.service-card').forEach(card => revealObserver.observe(card));

  // Active nav link highlight on scroll
  const sections = document.querySelectorAll('section[id]');
  const navItems = document.querySelectorAll('.nav-link');
  const sectionObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (!entry.isIntersecting) return;
      navItems.forEach(link => {
        link.style.color = '';
        if (link.getAttribute('href') === '#' + entry.target.id) {
          link.style.color = 'var(--gold2)';
        }
      });
    });
  }, { threshold: 0.45 });
  sections.forEach(s => sectionObserver.observe(s));

  // Contact form submission (demo — swap setTimeout for a real fetch/Formspree)
  const form = document.getElementById('contact-form');
  const successMsg = document.getElementById('form-success');
  form.addEventListener('submit', (e) => {
    e.preventDefault();
    const btn = form.querySelector('button[type="submit"]');
    btn.disabled = true;
    btn.innerHTML = '<i class="fa-solid fa-spinner fa-spin"></i> Sending…';
    setTimeout(() => {
      form.querySelectorAll('input, select, textarea').forEach(el => el.value = '');
      btn.innerHTML = '<i class="fa-solid fa-paper-plane"></i> Send Message';
      btn.disabled = false;
      successMsg.style.display = 'flex';
      setTimeout(() => { successMsg.style.display = 'none'; }, 5000);
    }, 1200);
  });

  // Smooth scroll fallback
  document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', (e) => {
      const target = document.querySelector(anchor.getAttribute('href'));
      if (target) {
        e.preventDefault();
        target.scrollIntoView({ behavior: 'smooth', block: 'start' });
      }
    });
  });
}
