// ── THEME ──────────────────────────────────────────────────
function applyTheme() {
  const r = document.documentElement;
  const p = CONFIG.colorPrimary;
  const a = CONFIG.colorAccent;

  // Darken primary for navy2, lighten accent for gold2
  r.style.setProperty('--navy',  p);
  r.style.setProperty('--navy2', lighten(p, 12));
  r.style.setProperty('--gold',  a);
  r.style.setProperty('--gold2', lighten(a, 18));
}

function lighten(hex, amt) {
  const num = parseInt(hex.replace('#', ''), 16);
  const r = Math.min(255, (num >> 16) + amt);
  const g = Math.min(255, ((num >> 8) & 0xff) + amt);
  const b = Math.min(255, (num & 0xff) + amt);
  return '#' + [r, g, b].map(v => v.toString(16).padStart(2, '0')).join('');
}

